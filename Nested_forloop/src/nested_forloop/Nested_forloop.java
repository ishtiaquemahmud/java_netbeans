package nested_forloop;

/**
 *
 * @author User
 */
public class Nested_forloop {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
       
        for(int i=9;i<=0;i--){
            for(int k=9;k<=0;k--){
                System.out.print(k+" ");
            }
            System.out.println();
        }
                      
    }
    }
    

